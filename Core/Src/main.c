/**
 * @brief  The application entry point.
 * @retval int
 */
int main(void)
{
	int status = NO_ERROR;

	/* USER CODE BEGIN 1 */

	/* USER CODE END 1 */

	/* MCU Configuration--------------------------------------------------------*/

	/* Reset of all peripherals, Initializes the Flash interface and the Systick. */
	HAL_Init();

	/* USER CODE BEGIN Init */

	/* USER CODE END Init */

	/* Configure the system clock */
	SystemClock_Config();

	/* USER CODE BEGIN SysInit */

	/* USER CODE END SysInit */

	/* Initialize all configured peripherals */
	MX_GPIO_Init();
	MX_ADC2_Init();
	MX_ADC3_Init();
	MX_CAN2_Init();
	MX_SPI1_Init();
	MX_USART3_UART_Init();
	MX_USB_DEVICE_Init();
	/* USER CODE BEGIN 2 */

	uint8_t onStat[3] = { 'M', '3', '\n' };
	uint8_t offStat[3] = { 'M', '5', '\n' };
	uint8_t ssStat[3] = { 'S', 'S', '\n' };
	uint8_t ack[3] = { 'A', 'C', '\n' };
	uint8_t errorMsg[3] = { 'E', 'R', '\n' };

	char initTx[] = "11111111111111111111"; //twenty 1s

	char *initTxPtr = initTx;

	uint8_t CDCtx[50] = { 'A', '2', '3', '4', '5', '6', '7', '\n' };

	char CDCrx[100];

	CDCrx[0] = 'a';

	char *CDCrxPtr1 = &CDCrx[2];

	uint32_t x = -99;
	int rpm = 0;

	//HAL_GPIO_WritePin(LED1_GPIO_Port, LED1_Pin, 1);
	while (CDCrx[0] != 'i')
	{
		//if this fails to build, revert usbd_cdc_if.h and .c from github
		CDC_Receive_FS((uint8_t*) CDCrx, &x);
		HAL_Delay(10);
	}
	CDC_Transmit_FS((uint8_t*) initTxPtr, 18);

	struct SpindleData spindle0;

	/* USER CODE END 2 */

	/* Infinite loop */
	/* USER CODE BEGIN WHILE */
	while (1)
	{
		/* USER CODE END WHILE */

		/* USER CODE BEGIN 3 */

		CDC_Receive_FS(CDCrx, &x);

		HAL_GPIO_WritePin(LED1_GPIO_Port, LED1_Pin, 1);
		HAL_Delay(40);
		HAL_GPIO_WritePin(LED1_GPIO_Port, LED1_Pin, 0);
		HAL_Delay(40);

		if (CDCrx[0] == 'M')
		{
			if (CDCrx[1] == '3')
			{
				CDC_Transmit_FS(ack, 3);

				//send the cmd to turn on the spindle
				status = H100spindleFWD(&huart3);

				if (status != NO_ERROR)
				{
					CDC_Transmit_FS(errorMsg, 3);
				}
				else
				{
					CDC_Transmit_FS(onStat, 3);
				}

				HAL_Delay(10);

			}
			else if (CDCrx[1] == '5')
			{
				CDC_Transmit_FS(ack, 3);

				if (H100spindleOFF(&huart3) != NO_ERROR)
				{
					CDC_Transmit_FS(errorMsg, 3);
				}
				else
				{
					CDC_Transmit_FS(offStat, 3);
				}
				HAL_Delay(10);

			}

		}
		else if (CDCrx[0] == 'S')
		{
			CDC_Transmit_FS(ack, 3);

			CDCrx[7] = (uint8_t) NULL;
			//rounddown ok
			rpm = atoi(CDCrxPtr1) / 3;

			//if message fails, send error message
			if (setFreq(rpm, &huart3))
			{
				CDC_Transmit_FS(errorMsg, 3);
			}
			else
			{
				CDC_Transmit_FS(ssStat, 3);
			}

		}
		else if (CDCrx[0] == 'R')
		{
			CDC_Transmit_FS(ack, 3);

			uint16_t spindleI;
			uint16_t spindleRPM;

			//if no errrors set vars

			switch (masterRd(&huart3, &spindle0))
			{
			case 0:
//				spindleI   = spindle0.current;
//				spindleRPM = spindle0.rpm;

				spindleI = altGetI();
				spindleRPM = altGetRPM();
				break;
			case 3:
				//HAL_TIMEOUT
				spindleI = 777;
				spindleRPM = 44666;
				break;
			case 4:
				//CRC failed
				spindleI = 888;
				spindleRPM = getRxCRC();
				break;
			default:
				spindleI = 999;
				spindleRPM = 44444;
				break;
			}

			//11 bytes long
			sprintf(CDCtx, "R%05d,%03d\n", spindleRPM, spindleI);

			CDC_Transmit_FS(CDCtx, 11);

			//delay needed since CDC tx is non blocking and OS is weird
			//HAL_Delay(20);
			//CDC_Transmit_FS(getCheck(), 11);
		}
		else if (CDCrx[0] == 'H') {
			if (CDCrx[1] == '3') {
				if (CDCrx[2] == '1') {
					unlock_Z_axis(&huart3);
				}
				else if (CDCrx[2] == '0') {
					lock_Z_axis(&huart3);
				}

			}


		}
		else if (CDCrx[0] == 'q') {
			//reboot
			HAL_NVIC_SystemReset();
		}
		//reset buffer
		CDCrx[0] = 'a';

	}

	/* USER CODE END 3 */
}

